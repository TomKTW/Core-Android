fun httpClient(): HttpClient {
    return HttpClient(Android) {
        install(Logging) {
            logger = Logger.DEFAULT
            level = LogLevel.HEADERS
        }
        engine {
            sslManager = {
                it.sslSocketFactory = getSSLContext().socketFactory
            }
        }
    }
}

