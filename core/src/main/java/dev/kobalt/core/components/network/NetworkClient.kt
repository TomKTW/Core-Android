fun getSSLContext(): SSLContext = SSLContext.getInstance("TLS").apply {
    val certificate =
        ByteArrayInputStream(certificateData.toByteArray()).use { certificateInputStream ->
            CertificateFactory.getInstance("X.509")
                .generateCertificate(certificateInputStream) as X509Certificate
        }
    val keyStore = KeyStore.getInstance(KeyStore.getDefaultType()).apply {
        load(null)
        setCertificateEntry(certificate.subjectX500Principal.name, certificate)
    }
    val keyManagers = KeyManagerFactory.getInstance("X509").apply {
        init(keyStore, null)
    }.keyManagers
    val trustManagers = TrustManagerFactory.getInstance("X509").apply {
        init(keyStore)
    }.trustManagers
    init(keyManagers, trustManagers, null)
}

fun getInsecureSSLContext(): SSLContext = SSLContext.getInstance("SSL").apply {
    init(null, arrayOf<TrustManager>(object : X509TrustManager {
        override fun getAcceptedIssuers(): Array<X509Certificate> = emptyArray()
        override fun checkClientTrusted(certs: Array<X509Certificate>, authType: String) = Unit
        override fun checkServerTrusted(certs: Array<X509Certificate>, authType: String) = Unit
    }), SecureRandom())
}

